W = [list(map(int, input().split())) for _ in range(10)]  # 读取10行数据，存入一个二维列表中


def chuli(i,ans):
    ans = list(ans)
    if i >= 0:
        ans[0] = '-'
        for k in range(7):
            if (i >> k) & 1 == 1:
                ans[8 - k - 1] = '1'
    else:
        ans[0] = '1'
        for k in range(7):
            if (128 + i >> k) & 1 == 1:
                ans[8 - k - 1] = '1'
    return ans

